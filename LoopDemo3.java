package com.suna.loop;

public class LoopDemo3 {
    public static void main(String[] args) {
        // do-while

        /*
            初始化语句；
            do{
                循环体语句；
                迭代语句；
            }while（循环条件）；

         */
        int i = 0;
        do{
            System.out.println("Hello World!");
            i++;
        }while(i < 5);
    }
}
